
public class Variable {

	public static void main(String[] args) {
		//변수: 변할 수 있는 값
		
		int a = 1; //integer 정수
		System.out.println(a);
		
		double b = 1.1; // double 실수 ... -2.0 -1.0 0 1.0 2.0 ...
		System.out.println(b);
		
		String c = "hihihi"; // String 문자열
		System.out.println(c);
		
		
	}

}
